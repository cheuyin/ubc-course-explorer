export async function deleteSection(req: Request, res: Response, repo: CourseSectionRepository): Promise<void> {
	const course = await repo.getCourseByIdAsync(req.params.courseId);
	if (course === undefined) {
		return sendNotFound(res, req.params.courseId, "course");
	}
	let section = await repo.getSectionByIdAsync(course, req.params.sectionId);
	if (section === undefined) {
		return sendNotFound(res, req.params.sectionId, "section");
	}
	section = await repo.deleteSectionAsync(course, req.params.sectionId);
	res.status(StatusCodes.OK).send({
		id: section.id,
		instructor: section.instructor,
		year: section.year,
		avg: section.avg,
		pass: section.pass,
		fail: section.fail,
		audit: section.audit,
	});
}
