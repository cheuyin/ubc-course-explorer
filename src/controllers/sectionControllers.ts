export async function createSection(req: Request, res: Response, repo: CourseSectionRepository): Promise<void> {
	const course = await repo.getCourseByIdAsync(req.params.courseId);
	if (course === undefined) {
		return sendNotFound(res, req.params.courseId, "course");
	}

	const errors = {
		error: "Validation failed",
		fields: {},
	};

	if (validateSectionBody(req, errors.fields)) {
		res.status(StatusCodes.UNPROCESSABLE_ENTITY).send(errors);
		return;
	}

	const section = await repo.getSectionByIdAsync(course, req.params.sectionId);
	const newSection = await repo.setSectionAsync(
		course,
		req.params.sectionId,
		req.body.instructor,
		req.body.year,
		req.body.avg,
		req.body.pass,
		req.body.fail,
		req.body.audit
	);
	if (section === undefined) {
		const body = formatSectionResponse(newSection, req.originalUrl);
		res.status(StatusCodes.CREATED).send(body);
	} else {
		res.sendStatus(StatusCodes.NO_CONTENT);
	}
}
