function validateSectionBody(req: Request, fields: any): any {
	const body = req.body;

	// instructor: required string
	if (body.instructor === undefined) {
		fields.instructor = "required but missing";
	} else if (typeof body.instructor !== "string") {
		fields.instructor = "expected a string";
	}

	// year: required integer 1900–2099
	const min = 1900;
	const max = 2099;
	if (body.year === undefined) {
		fields.year = "required but missing";
	} else if (typeof body.year !== "number" || !Number.isInteger(body.year) || body.year < min || body.year > max) {
		fields.year = "expected a number between 1900 and 2099";
	}

	// avg: required number 0–100
	if (body.avg === undefined) {
		fields.avg = "required but missing";
	} else if (typeof body.avg !== "number" || body.avg < 0 || body.avg > 100) {
		fields.avg = "expected a number between 0 and 100";
	}
	// pass, fail, audit: required integer >= 0
	["pass", "fail", "audit"].forEach((key) => {
		if (!(key in body)) {
			fields[key] = "required but missing";
		} else if (typeof body[key] !== "number" || body[key] < 0) {
			fields[key] = "expected a number >= 0";
		}
	});

	return Object.keys(fields).length > 0 ? fields : null;
}
